using System;
using System.Collections.Generic;
using System.Net;
using System.Net.Http;
using System.Text.Json;
using System.Threading;
using System.Threading.Tasks;

using Amazon.Lambda.APIGatewayEvents;

namespace NanoRoute.AwsLambda
{
    /// <summary>
    /// Routes API Gateway HTTP API and Lambda Function URL <see cref="APIGatewayHttpApiV2ProxyRequest"/> instances through a NanoRoute pipeline.
    /// </summary>
    /// <example>
    /// <code>
    /// ApiGatewayHttpApiV2Router router = ApiGatewayHttpApiV2Router
    ///     .CreateBuilder()
    ///     .AddJsonErrorDetails()
    ///     .AddDefaultValueParsers()
    ///     .AddHandler("GET", "/health/", (context, _) =&gt; HttpResponseMessage.Json(new { status = "ok" }))
    ///     .CreateRouter();
    /// </code>
    /// </example>
    public sealed class ApiGatewayHttpApiV2Router : Router<ApiGatewayHttpApiV2Router, AwsLambdaRouterConfig>
    {
        private ApiGatewayHttpApiV2Router(RouterBuilder<ApiGatewayHttpApiV2Router, AwsLambdaRouterConfig> builder) : base(builder) { }

        /// <summary>
        /// Routes an API Gateway HTTP API or Lambda Function URL payload-format-2.0 request and returns the corresponding proxy response.
        /// </summary>
        /// <param name="request">The API Gateway v2 request event.</param>
        /// <param name="services">The service provider exposed to handlers through <see cref="RequestContext.Services"/>.</param>
        /// <param name="remainingTime">The time left before the Lambda invocation times out. Expired execution produces a response with <see cref="HttpStatusCode.GatewayTimeout"/> status.</param>
        /// <returns>The API Gateway v2 proxy response generated by NanoRoute.</returns>
        /// <exception cref="ArgumentNullException">
        /// Thrown when <paramref name="request"/> or <paramref name="services"/> is <see langword="null"/>.
        /// </exception>
        /// <exception cref="ArgumentException">Thrown when the request uses an unsupported HTTP method.</exception>
        /// <exception cref="InvalidOperationException">Thrown when the API Gateway request does not contain a recognizable URI.</exception>
        /// <exception cref="HttpRequestException">
        /// Thrown when no handler matches the request path or a matched handler signals an HTTP failure that is not
        /// translated by middleware.
        /// </exception>
        /// <example>
        /// <code>
        /// APIGatewayHttpApiV2ProxyResponse response = await router.Route(request, services, context.RemainingTime);
        /// </code>
        /// </example>
        public async Task<APIGatewayHttpApiV2ProxyResponse> Route(APIGatewayHttpApiV2ProxyRequest request, IServiceProvider services, TimeSpan remainingTime)
        {
            Ensure.NotNull(request);
            Ensure.NotNull(services);

            TimeSpan cancellationDelay = remainingTime - Config.LambdaTimeoutBuffer;
            if (cancellationDelay <= TimeSpan.Zero)
                return Timeout();

            using CancellationTokenSource cts = new();
            cts.CancelAfter(cancellationDelay);

            using HttpRequestMessage requestMessage = request.CreateRequestMessage();

            try
            {
                using HttpResponseMessage responseMessage = await Handle(requestMessage, services, cts.Token);

                return await responseMessage.CreateResponse();
            }
            catch (OperationCanceledException)
            {
                return Timeout();
            }

            APIGatewayHttpApiV2ProxyResponse Timeout() => new()
            {
                StatusCode = (int) HttpStatusCode.GatewayTimeout,
                Body = JsonSerializer.Serialize
                (
                    new ErrorDetails
                    {
                        Status = HttpStatusCode.GatewayTimeout,
                        Title = "Gateway Timeout",
                        TraceId = request.RequestContext.RequestId
                    },
                    ErrorDetails.JsonTypeInfo
                ),
                Headers = new Dictionary<string, string>
                {
                    ["Content-Type"] = "application/json"
                }
            };
        }
    }
}
